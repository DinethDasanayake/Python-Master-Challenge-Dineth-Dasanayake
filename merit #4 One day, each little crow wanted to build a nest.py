'''
One day, each little crow wanted to build a nest.
Lady Crow wanted to build a nest of straw. Straw
costs Rs.4 a bundle. She needs 9 bundles. How
much will she get as a balance if she pays the
shop with a Rs.100 note?
'''
s_cost=4
bundles=9
total=s_cost*bundles

payment=100
balance=payment-total
print("Balance is Rs.",balance)